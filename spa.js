
// spa (single page application)

/*

A SPA application is a single page that continuously interacts with the user by dynamically rewriting 
the current page rather than loading entire new pages from a server.
    Trello, Facebook, Gmail, and Twitter are a few single page app examples.

*/

//  Single Page Application Pros:

/*  1.Quick Loading Time
    2.Seamless User Experience
    3.Ease in Building Feature-rich Apps
    4.Uses Less Bandwidth 
*/